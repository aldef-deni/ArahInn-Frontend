// Nama negara sesuai bahasa aktif (default Indonesia)
export const countryName = (c, lang) => (lang && lang.startsWith('en') ? c.en : c.id)

// Daftar terurut: Indonesia tetap teratas, sisanya alfabetis sesuai bahasa
export const sortedCountries = (lang) => {
  const [head, ...rest] = COUNTRIES
  return [head, ...rest.sort((a, b) => countryName(a, lang).localeCompare(countryName(b, lang)))]
}
